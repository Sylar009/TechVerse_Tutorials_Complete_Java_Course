class AdditionExample {
    public static void main(String[] args) {
        int value1 = 15, value2 = 25, total = 0;

        System.out.println("First Value = " + value1);
        System.out.println("Second Value = " + value2);

        total = value1 + value2;
        System.out.println("The Total = " + total);
    }
}